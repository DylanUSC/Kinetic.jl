export heaviside,
       fortsign


# ------------------------------------------------------------
# Heaviside step function
# ------------------------------------------------------------
heaviside(x::Real) = ifelse(x >= 0, 1., 0.)


# ------------------------------------------------------------
# Fortran sign() function
# ------------------------------------------------------------
fortsign(x::Real, y::Real) = abs(x) * sign(y)
