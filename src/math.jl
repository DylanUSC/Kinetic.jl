export heaviside,
       fortsign


# ------------------------------------------------------------
# Heaviside step function
# ------------------------------------------------------------
heaviside(x::Union{Int, Real}) = ifelse(x >= 0, 1., 0.)


# ------------------------------------------------------------
# Fortran sign() function
# ------------------------------------------------------------
fortsign(x::Union{Int, Real}, y::Union{Int, Real}) = abs(x) * sign(y)
